<p>
    {{__('In a consumer electronics market often clouded by vague environmental claims, 
    <b>OneTech</b> is gaining attention — not just for its cutting-edge smartphone design 
    but for its commitment to verified sustainability.') }}
</p>

<p>
    Earlier this year, the company released the <b>One Phone</b>, a premium modular smartphone 
    that combines high-end performance with eco-conscious engineering. According to the company, 
    the design allows users to easily replace and upgrade parts such as the battery, screen, 
    and camera — reducing the need for full-device replacement and minimizing e-waste.
</p>

<p>
    <i>“We’re committed to protecting the planet and designing products you love,”</i> 
    said Eliza Ng, OneTech’s Head of Sustainability. 
    <i>“Our modular approach and use of recycled and renewable materials are part of our mission 
    to achieve net-zero emissions across our carbon footprint.”</i>
</p>

<p>
    The company reports that <b>more than 30% of materials</b> shipped in its products in 2024 
    were sourced from <b>recycled or renewable resources</b>. In addition, 
    <b>the entire packaging of the One Phone is now 100% fiber-based</b>, the result of an initiative 
    to eliminate all plastics from the packaging process.
</p>

<p>
    <b>Packaging claim confirmed:</b> According to the latest environmental audit, OneTech has removed 
    plastic entirely from its product packaging and replaced it with recyclable fiber-based alternatives.
</p>

<p>
    But OneTech’s efforts don’t end at the point of sale. As part of its take-back program, the company offers 
    free recycling of old devices, cables, cases, and accessories — regardless of brand. Consumers can return 
    their used electronics in-store or via mail.
</p>

<blockquote class="border-l-4 border-green-500 pl-4 italic text-gray-700">
    “You’ll help protect the earth’s resources and reduce waste. We’re building a system where disposal 
    becomes circular, not wasteful.” — Eliza Ng
</blockquote>

<p>
    Unlike many competitors, OneTech has made its <b>full environmental product report</b> publicly available 
    on its website, detailing carbon emissions, materials sourcing, and lifecycle impact.
</p>

<div>
    <p class="mb-2">
        The brand has also received <b>official ecolabel certifications</b> from both Indonesian and international bodies, including:
    </p>
    <ul class="list-disc list-inside space-y-1">
        <li><b>LSE</b> – Indonesian Ecolabel Certification Body</li>
        <li><b>LVE</b> – Indonesian Ecolabel Verification Body</li>
        <li><b>BBSPJIS</b> – Cellulose Industry Standardization and Services Center</li>
        <li><b>EU Ecolabel</b> – European Union’s official mark of environmental excellence</li>
    </ul>
</div>

<p>
    These achievements have earned OneTech <b>high environmental performance ratings</b> from third-party evaluators — 
    a notable distinction in an industry often criticized for <i>greenwashing or greenhushing</i>.
</p>

<p>
    Environmental tech analyst Rudi Santoso says OneTech’s approach is “a blueprint other tech firms should follow.”
</p>

<p>
    <i>“They’re not just ticking boxes. They’re inviting scrutiny and setting a public benchmark for others 
    in the mobile device industry,”</i> he said.
</p>

<p>
    As the global demand for sustainable electronics grows, OneTech’s transparent and verifiable approach may serve 
    as a turning point — showing that innovation and accountability can go hand in hand.
</p>
